sub new {
	my $type  = shift;
	my $class = ref($type) || $type;
	my %superParms;
	my $this = ref($type) ? $type : {};
	my %parm  = @_;
	my %valid_parm = ( 'case_column' => 'm_SCALAR', 'bins' => 'SCALAR',
			'cook_scores' => 'REF', 'cdd_id' => 'SCALAR',
			'covariance_ratios' => 'REF', 'outside_n_sd' => 'REF',
			'bca_mode' => 'SCALAR', 'skipped_ids' => 'ARRAY',
			'skipped_keys' => 'ARRAY', 'skipped_values' => 'ARRAY',
			'selection_method' => 'SCALAR', 'logfile' => 'REF',
			'results_file' => 'SCALAR', 'prediction_models' => 'ARRAY',
			'cross_validate' => 'SCALAR', 'outside_n_sd_check' => 'SCALAR' );

	if( defined $parm{'reference_object'} ){
		foreach my $possible_parm( keys %valid_parm ){
			if( not exists $parm{$possible_parm} and not exists $this -> {$possible_parm} and exists $parm{'reference_object'} -> {$possible_parm} ){
				$parm{$possible_parm} = $parm{'reference_object'} -> {$possible_parm};
			}
		}
	}
	foreach my $givenp ( keys %parm ) {
		$superParms{$givenp} = $parm{$givenp} and next unless( defined $valid_parm{$givenp});

		if( $valid_parm{$givenp} =~ /^m_(.*)/ ){
			'debug' -> die( message => "ERROR in tool::cdd->new: You need to specify a \'$givenp\'!" )
				unless(defined $parm{$givenp} or defined $this -> {$givenp});
			$valid_parm{$givenp} = $1;
		}

		if( $valid_parm{$givenp} eq 'SCALAR' or $valid_parm{$givenp} eq 'REF' ){
			'debug' -> die( message => "ERROR in tool::cdd->new: " . $valid_parm{$givenp} . " parameter '$givenp' is of wrong type:" . ref(\$parm{$givenp}))
				if( defined $parm{$givenp} and ref(\$parm{$givenp}) ne $valid_parm{$givenp} );
		} elsif( $parm{$givenp} =~ /=HASH\(/ and $valid_parm{$givenp} ne '' ) {
			'debug' -> die( message => "ERROR in tool::cdd->new: " . lc($valid_parm{$givenp}) . " parameter '$givenp' is of wrong type:" . lc(ref($parm{$givenp})) )
				if( defined $parm{$givenp} and lc(ref($parm{$givenp})) ne lc($valid_parm{$givenp}));
		} elsif( $valid_parm{$givenp} ne '' ) {
			'debug' -> die( message => "ERROR in tool::cdd->new: " . $valid_parm{$givenp} . " parameter '$givenp' is of wrong type:" . ref($parm{$givenp}))
				if( defined $parm{$givenp} and ref($parm{$givenp}) ne $valid_parm{$givenp} );
		}
		$this -> {$givenp} = $parm{$givenp} unless defined $this -> {$givenp};
	}

	$this -> {'outside_n_sd'} = defined $parm{'outside_n_sd'} ? $parm{'outside_n_sd'} : [] unless defined $this -> {'outside_n_sd'};
	$this -> {'bca_mode'} = defined $parm{'bca_mode'} ? $parm{'bca_mode'} : 0 unless defined $this -> {'bca_mode'};
	$this -> {'logfile'} = defined $parm{'logfile'} ? $parm{'logfile'} : ['cdd.log'] unless defined $this -> {'logfile'};
	$this -> {'results_file'} = defined $parm{'results_file'} ? $parm{'results_file'} : 'cdd_results.csv' unless defined $this -> {'results_file'};
	$this -> {'prediction_models'} = defined $parm{'prediction_models'} ? $parm{'prediction_models'} : [] unless defined $this -> {'prediction_models'};
	$this -> {'cross_validate'} = defined $parm{'cross_validate'} ? $parm{'cross_validate'} : 1 unless defined $this -> {'cross_validate'};
	$this -> {'outside_n_sd_check'} = defined $parm{'outside_n_sd_check'} ? $parm{'outside_n_sd_check'} : 2 unless defined $this -> {'outside_n_sd_check'};

	bless $this, $class;
	tool::new($this,%superParms);

	# Start of Non-Dia code #
	# End of Non-Dia code #

	return $this;
};

